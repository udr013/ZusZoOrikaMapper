package com.udr013;


public class Yellow {

	int zo;
	String name;

	public int getZo() {
		return zo;
	}

	public void setZo(int zo) {
		this.zo = zo;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public String toString() {
		return "Yellow{" +
				"zo=" + zo +
				", name='" + name + '\'' +
				'}';
	}
}
